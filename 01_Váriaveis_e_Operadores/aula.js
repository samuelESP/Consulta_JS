//Váriaveis são espaços de memória reservado para algum tipo de dado
//Váriaveis podem ser 'let', 'var' ou 'const'
let x = 10;//BlockScoping; não faz Hoisting.
var y = 5; //escopo global; permite Hoisting.
const b = 2;//BlockScoping; não faz Hoisting; Como se trata de uma constante ela não deve ser alterada.
console.log(x, y, b);

// "=" é o valor de atribuição.
